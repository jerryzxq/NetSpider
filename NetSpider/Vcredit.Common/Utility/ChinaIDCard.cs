        /// <summary>
        /// 将老的身份证号码转化为新的身份证号码
        /// </summary>
        /// <param name="oldNumber">15或17位老身份证号码</param>
        /// <returns>新身份证号码</returns>
        public static string GetNewNumber(string p_OldNumber)
        {
            string _SfzNumber = p_OldNumber;
            if (p_OldNumber.Length == 15)
            {
                _SfzNumber = p_OldNumber.Substring(0, 6) + "19" + p_OldNumber.Substring(6, 9);
            }
            if (_SfzNumber.Length != 17) return "Error";

            char[] _TempString = _SfzNumber.ToCharArray();
            int[] _Value = new int[] { 7, 9, 10, 5, 8, 4, 2, 1, 6, 3, 7, 9, 10, 5, 8, 4, 2 };
            int _SumCount = 0;

            for (int i = 0; i != _TempString.Length; i++)
            {
                _SumCount += Int32.Parse(_TempString[i].ToString()) * _Value[i];
            }
            switch ((int)_SumCount % 11)
            {
                case 0:
                    return _SfzNumber + "1";
                case 1:
                    return _SfzNumber + "0";
                case 2:
                    return _SfzNumber + "X";
                case 3:
                    return _SfzNumber + "9";
                case 4:
                    return _SfzNumber + "8";
                case 5:
                    return _SfzNumber + "7";
                case 6:
                    return _SfzNumber + "6";
                case 7:
                    return _SfzNumber + "5";
                case 8:
                    return _SfzNumber + "4";
                case 9:
                    return _SfzNumber + "3";
                case 10:
                    return _SfzNumber + "2";
                default:
                    return "Error";
            }

        }
